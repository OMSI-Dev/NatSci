"""
Dataset Cache Loader
Module for loading and querying the dataset cache generated by sos_dataset_scanner.py

This runs on the REMOTE computer and reads the cache file generated on the SOS server.
"""

import json
import os
from datetime import datetime


class DatasetCache:
    """
    Class to load and query SOS dataset cache.
    """
    
    def __init__(self, cache_file_path):
        """
        Initialize and load dataset cache.
        
        Args:
            cache_file_path: Path to the JSON cache file
        """
        self.cache_file = cache_file_path
        self.cache_data = None
        self.datasets = {}
        self.loaded = False
        
        self.load()
    
    def load(self):
        """Load the cache file."""
        try:
            if not os.path.exists(self.cache_file):
                print(f"Warning: Cache file not found: {self.cache_file}")
                return False
            
            with open(self.cache_file, 'r', encoding='utf-8') as f:
                self.cache_data = json.load(f)
            
            self.datasets = self.cache_data.get('datasets', {})
            self.loaded = True
            
            # Print cache info
            scan_time = self.cache_data.get('scan_timestamp', 'unknown')
            total = self.cache_data.get('total_datasets', len(self.datasets))
            movies = self.cache_data.get('total_movies', 0)
            
            print(f"✓ Dataset cache loaded: {total} datasets, {movies} movies")
            print(f"  Cache generated: {scan_time}")
            
            return True
            
        except Exception as e:
            print(f"Error loading cache file: {e}")
            return False
    
    def is_movie(self, dataset_name):
        """
        Check if a dataset is a movie.
        
        Args:
            dataset_name: Name of the dataset
            
        Returns:
            bool: True if dataset is a movie, False otherwise
        """
        if not self.loaded or dataset_name not in self.datasets:
            return False
        
        return self.datasets[dataset_name].get('is_movie', False)
    
    def has_captions(self, dataset_name):
        """
        Check if a dataset has caption (.srt) files.
        
        Args:
            dataset_name: Name of the dataset
            
        Returns:
            bool: True if dataset has captions, False otherwise
        """
        if not self.loaded or dataset_name not in self.datasets:
            return False
        
        return self.datasets[dataset_name].get('has_captions', False)
    
    def get_srt_path(self, dataset_name):
        """
        Get the path to the .srt caption file for a dataset.
        
        Args:
            dataset_name: Name of the dataset
            
        Returns:
            str: Path to SRT file, or None if not found
        """
        if not self.loaded or dataset_name not in self.datasets:
            return None
        
        return self.datasets[dataset_name].get('srt_path')
    
    def get_srt_content(self, dataset_name):
        """
        Read and return the content of the .srt caption file.
        
        Note: This assumes the remote computer has access to the SOS filesystem
        (e.g., via network share). If not, you'll need to copy SRT files separately.
        
        Args:
            dataset_name: Name of the dataset
            
        Returns:
            str: SRT file content, or None if not found
        """
        srt_path = self.get_srt_path(dataset_name)
        
        if not srt_path or not os.path.exists(srt_path):
            return None
        
        try:
            with open(srt_path, 'r', encoding='utf-8') as f:
                return f.read()
        except Exception as e:
            print(f"Error reading SRT file {srt_path}: {e}")
            return None
    
    def get_dataset_info(self, dataset_name):
        """
        Get full information about a dataset.
        
        Args:
            dataset_name: Name of the dataset
            
        Returns:
            dict: Dataset info dictionary, or None if not found
        """
        if not self.loaded or dataset_name not in self.datasets:
            return None
        
        return self.datasets[dataset_name]
    
    def get_all_movies(self):
        """
        Get list of all movie dataset names.
        
        Returns:
            list: List of dataset names that are movies
        """
        if not self.loaded:
            return []
        
        return [name for name, info in self.datasets.items() if info.get('is_movie', False)]
    
    def get_movies_by_theme(self, theme):
        """
        Get list of movie datasets in a specific theme.
        
        Args:
            theme: Theme name (e.g., 'air', 'land', 'ocean')
            
        Returns:
            list: List of dataset names
        """
        if not self.loaded:
            return []
        
        return [name for name, info in self.datasets.items() 
                if info.get('is_movie', False) and info.get('theme') == theme]
    
    def print_summary(self):
        """Print a summary of the cache."""
        if not self.loaded:
            print("Cache not loaded")
            return
        
        print("\n" + "=" * 60)
        print("Dataset Cache Summary")
        print("=" * 60)
        print(f"Total datasets:     {len(self.datasets)}")
        print(f"Movie datasets:     {sum(1 for d in self.datasets.values() if d.get('is_movie'))}")
        print(f"Movies with captions: {sum(1 for d in self.datasets.values() if d.get('is_movie') and d.get('has_captions'))}")
        
        # Themes
        themes = set(d.get('theme') for d in self.datasets.values())
        print(f"Themes:             {', '.join(sorted(themes))}")
        print("=" * 60)


# Convenience functions for backward compatibility

_default_cache = None

def load_dataset_cache(cache_file_path):
    """
    Load dataset cache (module-level function).
    
    Args:
        cache_file_path: Path to JSON cache file
        
    Returns:
        DatasetCache: Cache object, or None on failure
    """
    global _default_cache
    _default_cache = DatasetCache(cache_file_path)
    return _default_cache if _default_cache.loaded else None


def is_movie_dataset(dataset_name, cache=None):
    """
    Check if dataset is a movie (module-level function).
    
    Args:
        dataset_name: Name of dataset
        cache: DatasetCache object (uses default if None)
    """
    if cache is None:
        cache = _default_cache
    
    return cache.is_movie(dataset_name) if cache else False


def get_dataset_srt_path(dataset_name, cache=None):
    """
    Get SRT path for dataset (module-level function).
    
    Args:
        dataset_name: Name of dataset
        cache: DatasetCache object (uses default if None)
    """
    if cache is None:
        cache = _default_cache
    
    return cache.get_srt_path(dataset_name) if cache else None


# Example usage
if __name__ == "__main__":
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: python dataset_cache_loader.py <cache_file>")
        print("\nExample:")
        print("  python dataset_cache_loader.py dataset_cache.json")
        sys.exit(1)
    
    cache_file = sys.argv[1]
    cache = DatasetCache(cache_file)
    
    if cache.loaded:
        cache.print_summary()
        
        # Show some examples
        movies = cache.get_all_movies()
        if movies:
            print(f"\nExample movie datasets (first 10):")
            for movie in movies[:10]:
                has_srt = "✓" if cache.has_captions(movie) else "✗"
                info = cache.get_dataset_info(movie)
                print(f"  {has_srt} [{info['theme']:12s}] {movie}")
